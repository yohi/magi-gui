class TestGenerateFilename:
    """Tests for generate_filename function"""

    def test_returns_markdown_extension(self):
        """generate_filename() should return .md extension"""
        filename = generate_filename("Test prompt")
        
        assert filename.endswith(".md")

    def test_includes_magi_prefix(self):
        """generate_filename() should include 'magi-report' prefix"""
        filename = generate_filename("Test prompt")
        
        assert filename.startswith("magi-report-")

    def test_includes_timestamp(self):
        """generate_filename() should include timestamp"""
        timestamp = datetime(2026, 1, 8, 15, 30, 45)
        
        filename = generate_filename("Test prompt", timestamp)
        
        assert "20260108-153045" in filename

    def test_uses_current_time_by_default(self):
        """generate_filename() should use current time when no timestamp provided"""
        filename1 = generate_filename("Test")
        filename2 = generate_filename("Test")
        
        # Both should have today's date
        today = datetime.now().strftime("%Y%m%d")
        assert today in filename1
        assert today in filename2

    def test_filename_includes_slug(self):
        """generate_filename() should include prompt slug"""
        filename = generate_filename("Hello World")
        assert "hello-world" in filename

    def test_filename_sanitizes_slug(self):
        """generate_filename() should sanitize prompt for slug"""
        filename = generate_filename("Hello @ World!")
        assert "hello-world" in filename
        assert "@" not in filename
        assert "!" not in filename

    def test_filename_truncates_long_slug(self):
        """generate_filename() should truncate long slugs"""
        long_prompt = "a" * 100
        filename = generate_filename(long_prompt)
        # 50 chars limit
        assert "a" * 50 in filename
        assert "a" * 51 not in filename

    def test_filename_preserves_japanese_characters(self):
        """generate_filename() should preserve Japanese characters"""
        filename = generate_filename("Êó•Êú¨Ë™û„ÉÜ„Çπ„Éà")
        assert "Êó•Êú¨Ë™û„ÉÜ„Çπ„Éà" in filename

    def test_filename_preserves_mixed_unicode_ascii(self):
        """generate_filename() should preserve mixed Unicode and ASCII"""
        filename = generate_filename("Hello ‰∏ñÁïå Test")
        assert "hello-‰∏ñÁïå-test" in filename

    def test_filename_handles_chinese_characters(self):
        """generate_filename() should preserve Chinese characters"""
        filename = generate_filename("‰∏≠ÊñáÊµãËØï")
        assert "‰∏≠ÊñáÊµãËØï" in filename

    def test_filename_handles_cyrillic_characters(self):
        """generate_filename() should preserve Cyrillic characters"""
        filename = generate_filename("–ü—Ä–∏–≤–µ—Ç –ú–∏—Ä")
        assert "–ø—Ä–∏–≤–µ—Ç-–º–∏—Ä" in filename

    def test_filename_removes_emoji_and_symbols(self):
        """generate_filename() should remove emoji but keep letters"""
        filename = generate_filename("Hello üëã World üåç")
        assert "hello-world" in filename
        assert "üëã" not in filename
        assert "üåç" not in filename

    def test_filename_handles_accented_characters(self):
        """generate_filename() should normalize accented characters"""
        filename = generate_filename("Caf√© r√©sum√©")
        # NFC normalization preserves composed accented characters
        # So "√©" is kept as "√©" in lowercase
        assert "caf√©" in filename
        assert "r√©sum√©" in filename

    def test_filename_collapses_multiple_separators(self):
        """generate_filename() should collapse multiple separators into one hyphen"""
        filename = generate_filename("Hello   @@@   World")
        assert "hello-world" in filename
        # Should not have multiple consecutive hyphens
        assert "--" not in filename

    def test_filename_handles_numbers_in_unicode(self):
        """generate_filename() should preserve numbers in various scripts"""
        filename = generate_filename("Test 123 „ÉÜ„Çπ„Éà")
        assert "test-123-„ÉÜ„Çπ„Éà" in filename
        assert "123" in filename

    def test_filename_empty_after_sanitization(self):
        """generate_filename() should handle prompts that become empty after sanitization"""
        filename = generate_filename("@#$%^&*()")
        # Should only have prefix and timestamp, no slug
        assert filename.startswith("magi-report-")
        # Should not have consecutive hyphens before timestamp
        import re
        assert not re.search(r"--", filename)

